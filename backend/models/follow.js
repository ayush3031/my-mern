const mongoose = require('mongoose');

const followSchema = new Schema({
    followerId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
    followingId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
  });

  const Follow = mongoose.model('follow',followSchema);

  module.exports = Follow;